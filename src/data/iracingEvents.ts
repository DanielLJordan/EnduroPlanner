export const ENDURANCE_EVENTS = IRACING_SPECIAL_EVENTS.filter(
  (e) => e.category === 'endurance'
)

export function searchEvents(query: string): IRacingEvent[] {
  if (!query.trim()) return IRACING_SPECIAL_EVENTS
  const q = query.toLowerCase()
  return IRACING_SPECIAL_EVENTS.filter(
    (e) =>
      e.name.toLowerCase().includes(q) ||
      e.shortName.toLowerCase().includes(q) ||
      e.track.toLowerCase().includes(q) ||
      e.carClasses.some((c) => c.toLowerCase().includes(q))
  )
}
